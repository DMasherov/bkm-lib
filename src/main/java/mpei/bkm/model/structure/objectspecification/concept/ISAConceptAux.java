package mpei.bkm.model.structure.objectspecification.concept;

public class ISAConceptAux {
    private String left;
    private String right;

    public ISAConceptAux(String left, String right) {
        this.left = left;
        this.right = right;
    }

    public String getLeft() {
        return left;
    }

    public String getRight() {
        return right;
    }
}
